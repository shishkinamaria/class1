function trialRoutineEnd(snapshot) {
  return async function () {
    //--- Ending Routine 'trial' ---
    trialComponents.forEach( function(thisComponent) {
      if (typeof thisComponent.setAutoDraw === 'function') {
        thisComponent.setAutoDraw(false);
      }
    });
    psychoJS.experiment.addData('trial.stopped', globalClock.getTime());
    // was no response the correct answer?!
    if (response_keyboard.keys === undefined) {
      if (['None','none',undefined].includes(corr_ans)) {
         response_keyboard.corr = 1;  // correct non-response
      } else {
         response_keyboard.corr = 0;  // failed to respond (incorrectly)
      }
    }
    // store data for current loop
    // update the trial handler
    if (currentLoop instanceof MultiStairHandler) {
      currentLoop.addResponse(response_keyboard.corr, level);
    }
    psychoJS.experiment.addData('response_keyboard.keys', response_keyboard.keys);
    psychoJS.experiment.addData('response_keyboard.corr', response_keyboard.corr);
    if (typeof response_keyboard.keys !== 'undefined') {  // we had a response
        psychoJS.experiment.addData('response_keyboard.rt', response_keyboard.rt);
        psychoJS.experiment.addData('response_keyboard.duration', response_keyboard.duration);
        routineTimer.reset();
        }
    
    response_keyboard.stop();
    // the Routine "trial" was not non-slip safe, so reset the non-slip timer
    routineTimer.reset();
    
    // Routines running outside a loop should always advance the datafile row
    if (currentLoop === psychoJS.experiment) {
      psychoJS.experiment.nextEntry(snapshot);
    }
    return Scheduler.Event.NEXT;
  }
}


